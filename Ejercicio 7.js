function concatenarArreglos(arreglo1, arreglo2) {
    return arreglo1.concat(arreglo2);
}

const arreglo1 = ["Hola", "Mundo"];
const arreglo2 = ["!", "¿Cómo", "estás?"];
console.log("Arreglo concatenado:", concatenarArreglos(arreglo1, arreglo2));